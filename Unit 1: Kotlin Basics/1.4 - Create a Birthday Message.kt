// Goal is to print a birthday cake using loops & nested loops as well as functions

fun printCakeTop(age: Int){
    repeat(age + 2){
        print("=");
        
    }
    println();
    
}

fun printCakeCandles(age: Int){
   print(" ");
   repeat(age){
       print(",");
       
   }
   println();
   
   print(" ");
   repeat(age){
       print("|");
       
   }
   println();
    
}

fun printCakeBottom(age: Int, layers: Int){
    
    repeat(layers){
        repeat(age + 2){
            print("@");

        }
        println();
    }
}

fun main(){
    var age = 24;
    var layers = 5;
    printCakeCandles(age);
    printCakeTop(age);
    printCakeBottom(age,layers);
    
}
